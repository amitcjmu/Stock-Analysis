import React from 'react';
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card';
import { Badge } from '@/components/ui/badge';
import { Alert, AlertDescription } from '@/components/ui/alert';
import { Database, Shield, AlertTriangle, Zap } from 'lucide-react';
import { useAuth } from '../../../contexts/AuthContext';
import { Skeleton } from '@/components/ui/skeleton';

// Modularized imports
import { useCrewAIInsights } from './hooks/useCrewAIInsights';
import { InfrastructurePatterns } from './components/InfrastructurePatterns';
import { MigrationReadiness } from './components/MigrationReadiness';
import { SixRRecommendations } from './components/SixRRecommendations';
import { TechnologyStackAnalysis } from './components/TechnologyStackAnalysis';
import { ActionableRecommendations } from './components/ActionableRecommendations';
import type { EnhancedInventoryInsightsProps } from './types/InventoryInsightsTypes';

// Types are now imported from the types file

const EnhancedInventoryInsights: React.FC<EnhancedInventoryInsightsProps> = ({
  assets,
  inventoryProgress,
  className = ""
}) => {
  const { client, engagement } = useAuth();
  
  // Use modularized hook for insights processing
  const { processedInsights, isLoading, error } = useCrewAIInsights();


  if (!client || !engagement) {
    return (
      <Card className={className}>
        <CardHeader>
          <CardTitle className="flex items-center gap-2">
            <Database className="h-5 w-5" />
            CrewAI Intelligence Insights
          </CardTitle>
        </CardHeader>
        <CardContent>
          <div className="text-center text-gray-500">
            Loading context...
          </div>
        </CardContent>
      </Card>
    );
  }

  if (isLoading) {
    return (
      <Card className={className}>
        <CardHeader>
          <CardTitle className="flex items-center gap-2">
            <Database className="h-5 w-5" />
            CrewAI Intelligence Insights
          </CardTitle>
        </CardHeader>
        <CardContent className="space-y-4">
          {[...Array(6)].map((_, i) => (
            <div key={i} className="space-y-2">
              <Skeleton className="h-4 w-3/4" />
              <Skeleton className="h-3 w-full" />
              <Skeleton className="h-3 w-5/6" />
            </div>
          ))}
        </CardContent>
      </Card>
    );
  }

  if (error || !processedInsights) {
    return (
      <Card className={className}>
        <CardHeader>
          <CardTitle className="flex items-center gap-2">
            <AlertTriangle className="h-5 w-5 text-orange-500" />
            CrewAI Intelligence Insights
          </CardTitle>
        </CardHeader>
        <CardContent>
          <div className="text-center text-gray-500">
            <p className="mb-2">No AI insights available yet</p>
            <p className="text-sm">CrewAI agents will generate insights as they analyze your inventory data</p>
          </div>
        </CardContent>
      </Card>
    );
  }

  const insights = processedInsights;

  return (
    <Card className={className}>
      <CardHeader>
        <CardTitle className="flex items-center gap-2">
          <Zap className="h-5 w-5 text-blue-500" />
          CrewAI Intelligence Insights
          <Badge variant="secondary" className="ml-auto">
            AI-Generated
          </Badge>
        </CardTitle>
      </CardHeader>
      <CardContent className="space-y-6">
        {/* Infrastructure Pattern Analysis */}
        <InfrastructurePatterns patterns={insights.infrastructure_patterns} />

        {/* Migration Readiness Assessment */}
        <MigrationReadiness readiness={insights.migration_readiness} />

        {/* 6R Strategy Recommendations */}
        <SixRRecommendations recommendations={insights.sixr_recommendations} />

        {/* Technology Stack Analysis */}
        <TechnologyStackAnalysis analysis={insights.technology_analysis} />

        {/* Actionable Recommendations */}
        <ActionableRecommendations recommendations={insights.actionable_recommendations} />

        {/* AI Confidence Indicator */}
        <div className="pt-4 border-t">
          <div className="flex items-center justify-between text-xs text-gray-500">
            <span className="flex items-center gap-1">
              <Shield className="h-3 w-3" />
              AI Analysis Confidence: High
            </span>
            <span>
              Generated by CrewAI Agents
            </span>
          </div>
        </div>
      </CardContent>
    </Card>
  );
};

export default EnhancedInventoryInsights; 